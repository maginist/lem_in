#include "libft.h"

void	ft_tabdel(void **tab)
{
	int	i;

	i = 0;
	if (!(tab) || !(*tab))
		return ;
	while (tab[i])
	{
		ft_memdel(&(tab[i]));
		i++;
	}
	free(tab);
}
